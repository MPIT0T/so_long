#include "ft_printf.h"

size_t	ft_strlen(const char *s)
{
	size_t	i;

	if (!s)
		return (0);
	i = 0;
	while (s[i])
		i++;
	return (i);
}

int	ft_putstr(const char *s)
{
	if (!s)
		return (ft_putstr("(null)"));
	return ((int) write(1, s, ft_strlen(s)));
}
